# Check whether the row of lottery numbers have the minimum ratio of odd / even numbers.
def isEvenOdd(lottoRow, minEvenNumbersDiff):
	even = 0
	for val in lottoRow:
		if val % 2 == 0:
			even += 1
		else:
			even -= 1
	if even <= minEvenNumbersDiff and even >= -minEvenNumbersDiff:
		return True
	return False

